package classiOggetti;

public class StudioEffettuato {
	String nomeScuola;
	int anni;
	boolean esame;
	
	public String getNomeScuola() {
		return nomeScuola;
	}
	public void setNomeScuola(String nomeScuola) {
		this.nomeScuola = nomeScuola;
	}
	public int getAnni() {
		return anni;
	}
	public void setAnni(int anni) {
		this.anni = anni;
	}
	public boolean isEsame() {
		return esame;
	}
	public void setEsame(boolean esame) {
		this.esame = esame;
	}
}
